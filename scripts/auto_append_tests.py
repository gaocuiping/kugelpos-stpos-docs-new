@dataclass
class ApiEndpoint:
    func_name: str
    method: str
    path: str
    docstring: str
    source_file: str
    path_params: list[str] = field(default_factory=list)

    def __post_init__(self):
        self.path_params = re.findall(r'\{(\w+)\}', self.path)

# ─────────────────────────────────────────────
# Step 1: API 解析
# ─────────────────────────────────────────────
def extract_api_endpoints(api_dir_abs: str) -> list[ApiEndpoint]:
    endpoints = []
    if not os.path.isdir(api_dir_abs):
        return endpoints

    pattern = re.compile(
        r'@router\.(?P<method>get|post|put|patch|delete)\s*\(\s*["\'](?P<path>[^"\']+)["\']'
        r'.*?'
        r'async\s+def\s+(?P<func_name>\w+)\s*\(',
        re.DOTALL | re.IGNORECASE,
    )

    for filename in sorted(os.listdir(api_dir_abs)):
        if not filename.endswith(".py") or filename == "__init__.py":
            continue
        filepath = os.path.join(api_dir_abs, filename)
        try:
            with open(filepath, "r", encoding="utf-8") as f:
                content = f.read()
            for m in pattern.finditer(content):
                func_pos = m.end()
                docstring = _extract_docstring_after_func(content[func_pos:])
                endpoints.append(ApiEndpoint(
                    func_name=m.group("func_name"),
                    method=m.group("method").upper(),
                    path=m.group("path"),
                    docstring=docstring,
                    source_file=filename,
                ))
        except Exception as e:
            print(f"  [ERROR] {filepath}: {e}")
    return endpoints

def _extract_docstring_after_func(text: str) -> str:
    doc_pattern = re.compile(r':\s*"""(.*?)"""', re.DOTALL)
    m = doc_pattern.search(text[:2000])
    if m:
        for line in m.group(1).strip().splitlines():
            if line.strip(): return line.strip()
    return ""

