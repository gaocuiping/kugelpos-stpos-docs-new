def build_url_fstring(path: str) -> str:
    url = ("/api/v1" + path) if not path.startswith("/api/") else path
    url = url.replace("{tenant_id}", "{test_tenant_id}").replace("{store_code}", "{test_store_code}")
    url = re.sub(r"\{(\w+)\}", lambda m: "{test_" + m.group(1) + "}", url)
    return f'f"{url}"'

