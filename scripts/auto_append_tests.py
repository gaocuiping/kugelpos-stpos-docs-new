def run_service(name: str, config: dict, dry_run: bool):
    api_dir = os.path.join(PROJECT_ROOT, config["api_dir"])
    tests_dir = os.path.join(PROJECT_ROOT, config["tests_dir"])
    print(f"\n=== サービス: {name} ===")

    endpoints = extract_api_endpoints(api_dir)
    ext_funcs, cov_funcs, urls = collect_existing_tests(tests_dir)

    to_gen = []
    for ep in endpoints:
        is_cov = ep.func_name in cov_funcs
        if not is_cov:
            for c in [f"test_{ep.func_name}", f"test_{ep.func_name}_auto"]:
                if c in ext_funcs: is_cov = True; break
        if not is_cov:
            norm = re.sub(r'\{[^}]+\}', '{var}', ep.path)
            if norm in urls or f"/api/v1{norm}" in urls: is_cov = True
        
        if is_cov:
            print(f"  [OK ] {ep.method:6s} {ep.path} ✅")
        else:
            print(f"  [NEW] {ep.method:6s} {ep.path} ❌")
            to_gen.append(ep)

    for ep in to_gen:
        for sub, is_scen in [("scenario", True), ("unit", False)]:
            d = os.path.join(tests_dir, sub)
            os.makedirs(d, exist_ok=True)
            if not os.path.exists(os.path.join(d, "__init__.py")): open(os.path.join(d, "__init__.py"), "w").close()
            fpath = os.path.join(d, f"test_{ep.func_name}_{sub}_auto.py")
            if os.path.exists(fpath): continue
            
            content = generate_full_test_content(ep, is_scen)
            if dry_run: print(f"  [DRY] {fpath}")
            else:
                with open(fpath, "w", encoding="utf-8") as f: f.write(content)
                print(f"  [NEW] {fpath} ✨")

def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--service", choices=list(SERVICES_CONFIG.keys()))
    parser.add_argument("--all", action="store_true")
    parser.add_argument("--dry-run", action="store_true")
    args = parser.parse_args()

    svcs = SERVICES_CONFIG.keys() if args.all else ([args.service] if args.service else ["cart"])
    for s in svcs: run_service(s, SERVICES_CONFIG[s], args.dry_run)

if __name__ == "__main__": main()
