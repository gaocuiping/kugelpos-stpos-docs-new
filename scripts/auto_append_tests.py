def collect_existing_tests(tests_dir_abs: str):
    existing_funcs, covered_funcs, urls = set(), set(), set()
    url_pattern = re.compile(r'\.(get|post|patch|put|delete)\s*\(\s*f?[\'"](/[^\'"\s{)]+)', re.I)

    for root, _, files in os.walk(tests_dir_abs):
        for fname in files:
            if not fname.endswith(".py"): continue
            try:
                with open(os.path.join(root, fname), "r", encoding="utf-8") as f:
                    content = f.read()
                for m in re.finditer(r'^\s*(?:async\s+)?def\s+(test_\w+)\s*\(', content, re.M):
                    existing_funcs.add(m.group(1))
                for m in re.finditer(r'# AUTO-GENERATED:\s*func_name=(\w+)', content):
                    covered_funcs.add(m.group(1))
                for m in url_pattern.finditer(content):
                    urls.add(re.sub(r'\{[^}]+\}', '{var}', m.group(2)).split('?')[0])
            except: continue
    return existing_funcs, covered_funcs, urls

