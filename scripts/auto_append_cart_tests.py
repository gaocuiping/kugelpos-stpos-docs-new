@dataclass
class ApiEndpoint:
    """API エンドポイント情報"""
    func_name: str
    method: str          # GET / POST / PATCH / DELETE / PUT
    path: str
    docstring: str
    source_file: str
    # path から抽出したパスパラメータ一覧（例：cart_id, lineNo）
    path_params: list[str] = field(default_factory=list)

    def __post_init__(self):
        self.path_params = re.findall(r'\{(\w+)\}', self.path)


# ─────────────────────────────────────────────
# Step 1: API 解析
# ─────────────────────────────────────────────
def extract_api_endpoints(api_dir_abs: str) -> list[ApiEndpoint]:
    """
    api_dir_abs 配下の .py ファイルから @router.xxx デコレータを解析して
    ApiEndpoint リストを返す。

    正規表現パターン：
      @router.<method>("<path>", ...)
      async def <func_name>(...):
          \"\"\"<docstring>\"\"\"
    """
    endpoints = []

    if not os.path.isdir(api_dir_abs):
        print(f"  [WARN] API ディレクトリが見つかりません: {api_dir_abs}")
        return endpoints

    pattern = re.compile(
        r'@router\.(?P<method>get|post|put|patch|delete)\s*\(\s*["\'](?P<path>[^"\']+)["\']'
        r'.*?'
        r'async\s+def\s+(?P<func_name>\w+)\s*\(',
        re.DOTALL | re.IGNORECASE,
    )

    for filename in sorted(os.listdir(api_dir_abs)):
        if not filename.endswith(".py") or filename == "__init__.py":
            continue

        filepath = os.path.join(api_dir_abs, filename)
        try:
            with open(filepath, "r", encoding="utf-8") as f:
                content = f.read()
        except Exception as e:
            print(f"  [ERROR] ファイル読み込み失敗: {filepath}: {e}")
            continue

        for m in pattern.finditer(content):
            func_name = m.group("func_name")
            method = m.group("method").upper()
            path = m.group("path")

            # docstring 抽出（関数定義の直後）
            # func_name 後ろの本文スライスから取得
            func_pos = m.end()
            remainder = content[func_pos:]
            docstring = _extract_docstring_after_func(remainder)

            endpoints.append(ApiEndpoint(
                func_name=func_name,
                method=method,
                path=path,
                docstring=docstring,
                source_file=filename,
            ))
            print(f"  [API] {method:6s} {path}  →  {func_name}()")

    return endpoints


def _extract_docstring_after_func(text: str) -> str:
    """関数定義直後の三重引用符ドキュメント文字列を抽出する"""
    # コロン + 空白/改行 + 三重引用符
    doc_pattern = re.compile(r':\s*"""(.*?)"""', re.DOTALL)
    m = doc_pattern.search(text[:2000])  # 先頭 2000 文字以内に限定
    if m:
        lines = m.group(1).strip().splitlines()
        # 最初の非空行だけを返す
        for line in lines:
            line = line.strip()
            if line:
                return line
    return ""


# ─────────────────────────────────────────────
# Step 2: 既存テスト関数の収集
# ─────────────────────────────────────────────
def collect_existing_test_funcs(tests_dir_abs: str) -> tuple[set[str], set[str], set[str]]:
    """
    tests_dir_abs 配下（再帰）の全 .py ファイルから以下を収集する：
      - 定義済みテスト関数名
      - AUTO-GENERATED タグが管理する API func_name
      - テストファイル内で呼び出されている URL パスパターン
        （例: /api/v1/carts, /carts/{cart_id}/lineItems）の断片集合

    戻り値：
      (existing_test_funcs, covered_api_funcs, url_fragments_by_file)
    """
    existing_test_funcs: set[str] = set()   # def test_xxx の xxx
    covered_api_funcs: set[str] = set()     # AUTO-GENERATED タグの func_name
    # テストファイル全体に出現する URL パス断片 (スラッシュ区切りのセグメント集合)
    all_url_segments: set[str] = set()

    # HTTP クライアント呼び出し行から URL を抽出するパターン
    # 例: .post("/api/v1/carts"), .get(f"/api/v1/carts/{cart_id}")
    url_call_pattern = re.compile(
        r'\.(get|post|patch|put|delete)\s*\(\s*f?[\'"](/[^\'"\s{)]+)',
        re.IGNORECASE,
    )

    for root, dirs, files in os.walk(tests_dir_abs):
        dirs[:] = [d for d in dirs if d != "__pycache__"]
        for fname in files:
            if not fname.endswith(".py"):
                continue
            fpath = os.path.join(root, fname)
            try:
                with open(fpath, "r", encoding="utf-8") as f:
                    content = f.read()
            except Exception:
                continue

            # def test_xxx を収集
            for m in re.finditer(r'^\s*(?:async\s+)?def\s+(test_\w+)\s*\(', content, re.MULTILINE):
                existing_test_funcs.add(m.group(1))

            # AUTO-GENERATED タグが管理する API func_name を収集
            for m in re.finditer(r'# AUTO-GENERATED:\s*func_name=(\w+)', content):
                covered_api_funcs.add(m.group(1))

            # HTTP 呼び出し URL のセグメントを収集
            for m in url_call_pattern.finditer(content):
                raw_path = m.group(2)
                # {variable} プレースホルダを {var} に正規化
                normalized = re.sub(r'\{[^}]+\}', '{var}', raw_path)
                # パス全体と各末尾セグメントを登録
                all_url_segments.add(normalized)
                # クエリパラメータを除去
                base = normalized.split('?')[0]
                all_url_segments.add(base)

    print(f"\n  [INFO] 既存テスト関数: {len(existing_test_funcs)} 件")
    print(f"  [INFO] AUTO-GEN タグ済み API: {len(covered_api_funcs)} 件")
    print(f"  [INFO] 検出 URL パターン: {len(all_url_segments)} 件")
    return existing_test_funcs, covered_api_funcs, all_url_segments


def _is_api_covered(
    endpoint: ApiEndpoint,
    existing_test_funcs: set[str],
    covered_api_funcs: set[str],
    all_url_segments: set[str],
) -> bool:
    """
    API がテスト済みかどうかを判定する。

    判定ルール（優先順）：
      1. covered_api_funcs に func_name がある → 済み（AUTO-GEN タグ）
      2. existing_test_funcs に test_<func_name> / test_<func_name>_auto がある → 済み
      3. URL パターン一致：エンドポイントのパスが既存テスト内の HTTP 呼び出しに含まれる → 済み
         （例: /carts/{cart_id}/lineItems がテスト内で呼ばれている → add_items は済み）
    """
    fn = endpoint.func_name

    # ルール 1: AUTO-GEN タグ
    if fn in covered_api_funcs:
        return True

    # ルール 2: 関数名一致
    for candidate in [f"test_{fn}", f"test_{fn}_auto", f"test_{fn}_happy_path"]:
        if candidate in existing_test_funcs:
            return True

    # ルール 3: URL パターン一致
    # API パスの {xxx} プレースホルダを {var} に正規化して比較
    normalized_api_path = re.sub(r'\{[^}]+\}', '{var}', endpoint.path)
    # /api/v1 プレフィックスを付けたバージョンも比較
    prefixed = f"/api/v1{normalized_api_path}"

    if normalized_api_path in all_url_segments or prefixed in all_url_segments:
        return True

    # パス末尾セグメントでの部分一致（フラグメントレベル）
    # 例：/carts/{var}/lineItems/{var}/cancel が含まれるか
    path_segments = normalized_api_path.rstrip('/').split('/')
    # 末尾の固定セグメント（パラメータでない部分）で補助一致
    fixed_tail = '/'.join([s for s in path_segments if s and '{' not in s])
    if fixed_tail and len(fixed_tail) > 3:  # 短すぎる文字列は除外
        for seg in all_url_segments:
            if fixed_tail in seg:
                return True

    return False


