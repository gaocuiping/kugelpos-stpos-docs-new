def run(service_name: str, dry_run: bool = False):
    """指定サービスの API スキャン → テスト差分生成を実行する"""
    config = SERVICES_CONFIG.get(service_name)
    if not config:
        print(f"[ERROR] サービス '{service_name}' が設定に見つかりません。")
        return

    api_dir_abs   = os.path.join(PROJECT_ROOT, config["api_dir"])
    tests_dir_abs = os.path.join(PROJECT_ROOT, config["tests_dir"])

    print(f"\n{'='*60}")
    print(f"  サービス: {service_name}")
    print(f"  API  DIR: {api_dir_abs}")
    print(f"  TEST DIR: {tests_dir_abs}")
    print(f"  DRY RUN : {dry_run}")
    print(f"{'='*60}\n")

    # 1. API スキャン
    print("【STEP 1】API エンドポイントをスキャン中...")
    endpoints = extract_api_endpoints(api_dir_abs)
    print(f"\n  → {len(endpoints)} 件の API エンドポイントを検出\n")

    # 2. 既存テスト収集
    print("【STEP 2】既存テスト関数を収集中...")
    existing_test_funcs, covered_api_funcs, all_url_segments = collect_existing_test_funcs(tests_dir_abs)

    # 3. 差分計算 + 生成
    print("\n【STEP 3】差分計算 & テスト骨架生成...")
    uncovered = []
    for ep in endpoints:
        if _is_api_covered(ep, existing_test_funcs, covered_api_funcs, all_url_segments):
            print(f"  [OK  ] {ep.method:6s} {ep.path}  →  {ep.func_name}()  ✅ 既にカバー済み")
        else:
            print(f"  [NEW ] {ep.method:6s} {ep.path}  →  {ep.func_name}()  ❌ 未カバー → 生成対象")
            uncovered.append(ep)

    print(f"\n  未カバー: {len(uncovered)} / {len(endpoints)} エンドポイント")

    if not uncovered:
        print("\n  ✅ 全エンドポイントがカバー済みです。スキップします。")
        return

    print(f"\n【STEP 4】テストファイルを生成中... (dry_run={dry_run})")
    total_generated = []
    for ep in uncovered:
        print(f"\n  → {ep.func_name}() の骨架を生成:")
        generated = generate_test_files(ep, tests_dir_abs, dry_run=dry_run)
        total_generated.extend(generated)

    # サマリー
    print(f"\n{'='*60}")
    print(f"  完了！")
    print(f"  対象 API    : {len(endpoints)} 件")
    print(f"  カバー済み  : {len(endpoints) - len(uncovered)} 件  ✅")
    print(f"  新規生成    : {len(uncovered)} 件  ✨  ({len(total_generated)} ファイル)")
    if dry_run:
        print(f"  ※ DRY-RUN モードのため、実際のファイルは作成されていません。")
    print(f"{'='*60}\n")

    if not dry_run and total_generated:
        print("  次のステップ：")
        print("    1. 生成されたファイルを開き、pytest.skip() を実装に置き換えてください。")
        print("    2. pytest テストを実行して収集できることを確認してください：")
        print(f"       cd {os.path.dirname(tests_dir_abs)}")
        print(f"       pytest tests/ --collect-only 2>&1 | grep 'auto'")
        print()


def main():
    parser = argparse.ArgumentParser(
        description="全サービス API 変更に基づくテスト骨架自動追加スクリプト"
    )
    parser.add_argument(
        "--service",
        choices=list(SERVICES_CONFIG.keys()),
        help="対象サービス名（例: cart, terminal）",
    )
    parser.add_argument(
        "--all",
        action="store_true",
        help="全サービスを対象にする",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="ドライラン：ファイルを作成せず確認のみ行う",
    )
    args = parser.parse_args()

    if args.all:
        # 全サービスを順番に処理
        for svc in SERVICES_CONFIG.keys():
            run(service_name=svc, dry_run=args.dry_run)
    elif args.service:
        run(service_name=args.service, dry_run=args.dry_run)
    else:
        # デフォルト: cart（後方互換性のため）
        run(service_name="cart", dry_run=args.dry_run)


if __name__ == "__main__":
    main()
